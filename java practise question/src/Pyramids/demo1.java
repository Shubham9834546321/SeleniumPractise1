package Pyramids;

public class demo1 {

	public static void main(String[] args) {
		for(int i=1;i<=4;i++) {                // i=4
		                                       // j=5
			for(int j=1;j<=i;j++) {           //
				System.out.print("*");
			}
			System.out.println();                    //*
			                                         //* *
			                                         //* * *
			                                         //* * * *
			                                          
		}
		

	}

}

